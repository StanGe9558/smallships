    @Override
    public void setRotationAngles(T entityIn, float limbSwing, float limbSwingAmount, float ageInTicks, float netHeadYaw, float headPitch) {
        if (entityIn.getSailState()) {
            this.Sail_1_0.showModel = true;
            this.Sail_2_0.showModel = true;
            this.Sail_1_0_o.showModel = true;
            this.Sail_2_0_o.showModel = true;
            this.Sail_0_1.showModel = false;
            this.Sail_0_2.showModel = false;
            this.Sail_0_3.showModel = false;
            this.Sail_0_4.showModel = false;

        } else {
            this.Sail_0_1.showModel = true;
            this.Sail_0_2.showModel = true;
            this.Sail_0_3.showModel = true;
            this.Sail_0_4.showModel = true;
            this.Sail_1_0.showModel = false;
            this.Sail_2_0.showModel = false;
            this.Sail_1_0_o.showModel = false;
            this.Sail_2_0_o.showModel = false;
        }
        if (entityIn.getSteerState(0)){
            this.steer.rotateAngleX = -MathHelper.cos(3.1415927F);
            //this.Brigg.rotateAngleZ = -MathHelper.cos(3.1415927F * 2 );
        }
        else if (entityIn.getSteerState(1)) {
            this.steer.rotateAngleX = MathHelper.cos(3.1415927F);
            //this.Brigg.rotateAngleZ = MathHelper.cos(3.1415927F * 2 );
        }
        else {
            this.steer.rotateAngleX = 0;
            //this.Brigg.rotateAngleZ = 0;
        }

        this.Cargo0.showModel = (entityIn).Cargo_0;
        this.Cargo1.showModel = (entityIn).Cargo_1;
        this.Cargo2.showModel = (entityIn).Cargo_2;
        this.Cargo3.showModel = (entityIn).Cargo_3;
    }

