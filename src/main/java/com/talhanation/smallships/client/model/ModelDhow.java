    @Override
    public void setupAnim(T entityIn, float limbSwing, float limbSwingAmount, float ageInTicks, float netHeadYaw, float headPitch) {
        if (entityIn.getSailState()) {
            this.seil_0.visible = true;
            this.segel_1.visible = true;
            this.segel_2.visible = true;
            this.Sail_0_1.visible = false;
            this.Sail_0_2.visible = false;

        } else {
            this.seil_0.visible = false;
            this.segel_1.visible = false;
            this.segel_2.visible = false;
            this.Sail_0_1.visible = true;
            this.Sail_0_2.visible = true;

        }
        if (entityIn.getSteerState(0)){
            this.steer.xRot = -MathHelper.cos(3.1415927F);
            //this.Brigg.rotateAngleZ = -MathHelper.cos(3.1415927F * 2 );
        }
        else if (entityIn.getSteerState(1)) {
            this.steer.xRot = MathHelper.cos(3.1415927F);
            //this.Brigg.rotateAngleZ = MathHelper.cos(3.1415927F * 2 );
        }
        else {
            this.steer.xRot = 0;
            //this.Brigg.rotateAngleZ = 0;
        }

        this.Cargo0.visible = (entityIn).Cargo_0;
        this.Cargo1.visible = (entityIn).Cargo_1;
        this.Cargo2.visible = (entityIn).Cargo_2;
        this.Cargo3.visible = (entityIn).Cargo_3;
    }

